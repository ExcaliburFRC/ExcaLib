package frc.excalib.control.motor.motor_specs;

/**
 * Represents the direction state of a motor.
 * This enum is used to specify whether the motor is moving in the
 * forward or reverse direction.
 */
public enum DirectionState {
    /**
     * Indicates that the motor is moving in the forward direction.
     */
    FORWARD,

    /**
     * Indicates that the motor is moving in the reverse direction.
     */
    REVERSE;
}